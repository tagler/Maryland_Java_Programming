package assignments.intro;

public class AddsNumbers {
	
    public static void main(String args[]) {
        int a = 17, b = 4, c;
        // adds
        c = a + b;
        System.out.println(a + " + " + b + " = " + c);
        // divides
        c = a / b;
        System.out.println(a + " / " + b + " = " + c);
        // mod
        c = a % b;
        System.out.println(a + " % " + b + " = " + c);
    }
    
}

